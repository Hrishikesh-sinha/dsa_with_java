package Basic_In_Java;

public class increamentanddecrement {
    public static void main(String[] args) {
        int sneha = 10;
        sneha++;
        System.out.println(sneha);

        int swetangi = 77;
        ++swetangi;
        System.out.println(swetangi);

        int x = 100;
        System.out.println(x);
        System.out.println(x++);
        System.out.println(x);

        int a = 8;
        int b = a++;
        System.out.println(a+" "+b);
    }
}
